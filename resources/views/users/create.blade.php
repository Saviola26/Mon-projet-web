    <script>
        // Fonction qui bascule l'affichage du champ 'code_secret' en fonction du rôle sélectionné
        function toggleCodeSecretField() {
            var role = document.getElementById("role").value;
            var codeSecretField = document.getElementById("code_secret_field");

            if (role === "admin") {
                codeSecretField.style.display = "block";
            } else {
                codeSecretField.style.display = "none";
            }
        }

        // Appel initial au cas où le rôle par défaut serait "admin"
        document.addEventListener('DOMContentLoaded', toggleCodeSecretField);
    </script>
